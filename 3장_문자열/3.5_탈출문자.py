# \n: 줄바꿈
print("안녕하세요\n반갑습니다")

# 문장안에 "", ''
print("저는 '안동주' 입니다")
print('저는 "안동주" 입니다')

# \" \' : 문장 내에서 따옴표 사용가능
print("저는 \'안동주\' 입니다")
print("저는 \"안동주\" 입니다")

# \\ : 문장 내에서 \
print("C:Users\\abcd\\abcd\\python")

# /r : 커서를 맨 앞으로 이동
# Red Apple이 먼저 찍히고 커서가 앞으로 돌아와서 Red 자리에 Pine출력
print("Red Apple\rPine")  # PineApple출력

# \b : 백스페이스(한 글자 삭제)
print("Pinee\bApple")  # PineApple출력

# \t: 탭
print("Red\tApple")  # Red   Apple출력
